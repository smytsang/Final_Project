let tileGrid = [];

let platform = 240;
let platformIndex = tileGridIndex(13, 28);
let moveSpeed = 1.5;
// let platformCenter = (platform + rightEdge)/2;
let topEdge = 540;

function platformMove() {
  let rightEdge = platform+(TILE_W*4);
  let leftEdge = platform;
  let topEdge = 540;

  if (isTileAtPixel(platform, topEdge+TILE_H*2) == 1) {
    moveSpeed *= -1;
  } else if (isTileAtPixel(rightEdge, topEdge+TILE_H*2) == 1) {
    moveSpeed *= -1;
  }
  platform += moveSpeed;
}

// Adds an index to all values in the grid
function tileGridIndex(tileCol, tileRow) {
  return (tileCol + TILE_COLS*tileRow);
}

function isTileHere(tileCol, tileRow) {
  let tileIndex = tileGridIndex(tileCol, tileRow);
  // returns true if there is a tile of any kind
  return (tileGrid[tileIndex] != 0);
}

function isTileAtPixel(pixelX, pixelY) {
  let tileCol = Math.floor(pixelX / TILE_W);
  let tileRow = Math.floor(pixelY / TILE_H);

  if(tileCol < 0 || tileCol >= TILE_COLS || tileRow < 0 || tileRow >= TILE_ROWS) {
    return false;
  }

  let tileIndex = tileGridIndex(tileCol, tileRow);
  return (tileGrid[tileIndex]);
}

function drawTiles() {
  for (let col = 0; col < TILE_COLS; col++) {
    for (let row = 0; row < TILE_ROWS; row++) {
      if (isTileHere(col, row)) {
        let tileLeftEdge = col * TILE_W;
        let tileTopEdge = row * TILE_H;

        let tileIndex = tileGridIndex(col, row)
        if (tileGrid[tileIndex] == 1) {
          canvasContext.drawImage(wallPic, tileLeftEdge, tileTopEdge, TILE_W-TILE_GAP, TILE_H-TILE_GAP)
        } else if (tileGrid[tileIndex] == 2) {
          colorRect(tileLeftEdge, tileTopEdge, TILE_W, TILE_H, 'blue')
        } else if (tileGrid[tileIndex] == 3) {
          canvasContext.drawImage(starPic, tileLeftEdge+(TILE_W/3)-3, tileTopEdge+(TILE_H/2)-3, TILE_W/2, TILE_H/2)
        } else if (tileGrid[tileIndex] == 4) {
          canvasContext.drawImage(doorPic, 10, 25, 70, 55)
        } else if (tileGrid[tileIndex] == 5) {
          canvasContext.drawImage(platformPic, platform, tileTopEdge, TILE_W*4, TILE_H/2)
        } else if (tileGrid[tileIndex] == 6) {
          canvasContext.drawImage(mario, marioLeft, marioTop, 25, 25)
        } else if (tileGrid[tileIndex] == 7) {
          canvasContext.drawImage(tubePic, tileLeftEdge, tileTopEdge+TILE_H/2, TILE_W*2.5, TILE_H*3)
        }
      }
    }
  }
}
